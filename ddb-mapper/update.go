// WithTableNameOverride overrides the table name.
func (opts *UpdateOptions) WithTableNameOverride(tableName string) *UpdateOptions {
	opts.tableName = &tableName
	return opts
}

// WithInputOptions modifies the [dynamodb.UpdateItemInput] parameters right before invoking DynamoDB.
//
// Don't use WithInputOptions to modify [dynamodb.UpdateItemInput.ConditionExpression],
// [dynamodb.UpdateItemInput.UpdateExpression], [dynamodb.UpdateItemInput.ExpressionAttributeNames], and/or
// [dynamodb.UpdateItemInput.ExpressionAttributeNames] since they are most likely already filled out by the time fn
// runs. Instead, use And and Or which are designed to work in tandem therewith.
func (opts *UpdateOptions) WithInputOptions(fn func(input *dynamodb.UpdateItemInput)) *UpdateOptions {
	opts.inputFn = fn
	return opts
}

// WithClientOptions attaches options to the [dynamodb.Client.UpdateItem] invocation.
func (opts *UpdateOptions) WithClientOptions(optFns ...func(opts *dynamodb.Options)) *UpdateOptions {
	opts.optFns = optFns
	return opts
}

